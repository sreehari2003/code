function uniquePathsWithObstacles(obstacleGrid: number[][]): number {
  const rows = obstacleGrid.length;
  const cols = obstacleGrid[0].length;
  const dp = {};

  if (obstacleGrid[rows - 1][cols - 1] === 1) return 0;

  const dfs = (r: number, c: number, x: number, y: number) => {
    const index = `${x},${y}`;

    if (index in dp) {
      return dp[index];
    }
    if (x === 1 && y === 1) return 1;
    if (x == 0 || y == 0) return 0;
    if (obstacleGrid[r][c] === 1) return 0;

    dp[index] = dfs(r + 1, c, x - 1, y) + dfs(r, c + 1, x, y - 1);

    return dp[index];
  };

  return dfs(0, 0, rows, cols);
}

